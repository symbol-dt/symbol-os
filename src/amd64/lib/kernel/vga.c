#include <kernel/vga.h>
#include <kernel/asm.h>
#include <stddef.h>

void init_vga(void){
    uint8_t crtc11;
    // 解锁CRTC寄存器（索引0x11）
    outb(0x3D4, 0x11);
    crtc11 = inb(0x3D5);
    outb(0x3D5, crtc11 & 0x7F); // 清除保护位（Bit 7）
    // 2. 配置序列发生器
    outb(0x3C4, 0x04);
    outb(0x3C5, 0x03); // Chain 4 + Odd/Even
    // 3. 配置图形控制器（显存映射到 0xA0000）
    outb(0x3CE, 0x06);
    outb(0x3CF, 0x01); // 0xA0000
    // 4. 配置 CRTC 寄存器（40×25）
    outb(0x3D4, 0x00); outb(0x3D5, 0x5F);
    outb(0x3D4, 0x01); outb(0x3D5, 0x4F);
    outb(0x3D4, 0x02); outb(0x3D5, 0x50);
    outb(0x3D4, 0x03); outb(0x3D5, 0x82);
    outb(0x3D4, 0x04); outb(0x3D5, 0x0B);
    outb(0x3D4, 0x05); outb(0x3D5, 0x3E);
    outb(0x3D4, 0x06); outb(0x3D5, 0x25);
    outb(0x3D4, 0x07); outb(0x3D5, 0x0F);
    outb(0x3D4, 0x09); outb(0x3D5, 0x07);
    // 5. 配置属性控制器（单色文本）
    inb(0x3DA); // 重置 Attribute Controller
    outb(0x3C0, 0x10);
    outb(0x3C0, 0x0C); // 文本模式
    outb(0x3C0, 0x20); // 启用 Attribute Controller
    // 6. 恢复CRTC保护位（可选）
    outb(0x3D4, 0x11);
    outb(0x3D5, crtc11);
}